public class AdjacentCells {

	int presentCell;
	int cellOnRight;
	int cellOnBottom;
	
	public int getPresentCell() {
		return presentCell;
	}
	public void setPresentCell(int presentCell) {
		this.presentCell = presentCell;
	}
	public int getCellOnRight() {
		return cellOnRight;
	}
	public void setCellOnRight(int cellOnRight) {
		this.cellOnRight = cellOnRight;
	}
	public int getCellOnBottom() {
		return cellOnBottom;
	}
	public void setCellOnBottom(int cellOnBottom) {
		this.cellOnBottom = cellOnBottom;
	}
}
